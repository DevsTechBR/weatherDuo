import "./index.scss";
import Input from "../Input/Input";
import CardInput from "../CardInput/CardInput";
import { useEffect, useState } from "react";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export default function WeatherContent() {
  const [city, setCity] = useState("");
  const [onSearchCity, setOnSearchCity] = useState(false);
  const [weatherData, setWeatherData] = useState(null);

  useEffect(() => {
    async function getCity() {
      let dataResponse = await fetch(
        `https://api.weatherapi.com/v1/forecast.json?key=e63768fb567b4a2b822125329231105&q=${city}&lang=pt`
      );
      let data = await dataResponse.json();
      if (data.error) {
        toast.error("Cidade nÃ£o encontrada!");
        return;
      }
      if (data) {
        toast.success("Cidade encontrada!");
      }
      setWeatherData(data);
      setCity("");
    }

    if (onSearchCity == true) {
      getCity();
      setOnSearchCity(false);
    }
  }, [onSearchCity]);

  const searchCity = (canSearch) => {
    setOnSearchCity(canSearch);
  };

  return (
    <>
      <ToastContainer autoClose={1000} theme="dark" />
      {weatherData && (
        <CardInput
          onSearchCity={onSearchCity}
          setOnSearchCity={setOnSearchCity}
          city={city}
          weatherData={weatherData}
        />
      )}
      <Input setCity={setCity} searchCity={searchCity} city={city} />
    </>
  );
}

